<div class="custom-hr">
	<span class="hr-left"></span>
</div>

<div class="row">
    <div class="col-lg-12 col-s-12">
        <p>
            <img class="side-image right" src="<?php echo get_template_directory_uri(); ?>/img/zur-person/zur-person-top.jpg" alt="">
            Ich bin 1962 geboren und wuchs als älteste von fünf Töchtern auf einem Bauernhof in Schwarzberg, einem kleinen Dorf in der Marktgemeinde Wernberg-Köblitz auf.
            <br><br>

            Nach Abschluss des 2. juristischen Staatsexamens arbeitete ich als Landesgeschäftsführerin der Katholischen Landjugendbewegung Bayerns.<br><br>

            Von 1994 bis 2005 war ich Mitglied des Bayerischen Landtages und dort Vorsitzende und stellvertretende Vorsitzende des Ausschusses "Jugend, Bildung und Sport" sowie bildungspolitische Sprecherin der SPD-Landtagsfraktion. Acht Jahre lang gehörte ich dem Landwirtschafts- und dem Rechtsausschuss des Bayerischen Landtags an.<br><br>

            Seit 2005 bin ich Mitglied des Deutschen Bundestages. Hier war ich unter anderem in den Ausschüssen für Ernährung, Landwirtschaft und Verbraucherschutz, für Bildung, Forschung und Technikfolgenabschätzung sowie im Rechtsausschuss tätig. Eine Liste meiner aktuellen Aufgaben und Mitgliedschaften finden Sie unter <a href="<?php echo get_site_url(); ?>/bundestag">Bundestag</a>.<br><br>
            Auch kommunalpolitisch bin ich aktiv: Seit 1996 bin ich Mitglied im Marktrat meiner Heimatgemeinde Wernberg-Köblitz und im Kreistag des Landkreises Schwandorf.<br><br>

            Zur Politik kam ich über mein langjähriges ehrenamtliches Engagement in der Katholischen Landjugendbewegung und durch meine Erfahrungen in der Bürgerbewegung gegen die geplante WAA in Wackersdorf.<br><br>
            1990 bin ich der SPD beigetreten und bin Vorsitzende meines Heimat-SPD-Ortsvereins Wernberg-Köblitz. Daneben bin ich Vorsitzende des SPD-Unterbezirks Schwandorf-Cham und stellvertretende Bezirkvorsitzende der Oberpfälzer SPD. Von 2009 bis 2017 war ich Mitglied im Landesvorstand der BayernSPD und von 2011 bis 2017 außerdem Mitglied des Präsidiums der BayernSPD.<br><br>

        </p>
