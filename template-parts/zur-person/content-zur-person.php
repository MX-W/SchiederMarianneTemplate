<h1 class="section-heading">Persönlicher Werdegang</h1>

<div class="custom-hr">
	<span class="hr-left"></span>
</div>

<div class="row justified">
    <div class="col-lg-6 col-s-12 col-left">
        <p>
            Ich bin 1962 geboren und wuchs als älteste von fünf Töchtern auf einem Bauernhof in Schwarzberg, einem kleinen Dorf in der Marktgemeinde Wernberg-Köblitz auf. <br> <br>

            Nach Abschluss des 2. juristischen Staatsexamens arbeitete ich als Landesgeschäftsführerin der Katholischen Landjugendbewegung Bayerns. <br> <br>

            1994 - 2005 war ich Mitglied des Bayerischen Landtages und dort Vorsitzende und stellvertretende Vorsitzende des Ausschusses "Jugend, Bildung und Sport" sowie bildungspolitische Sprecherin der SPD-Landtagsfraktion. Acht Jahre lang gehörte ich dem Landwirtschafts- und dem Rechtsausschuss des Bayerischen Landtags an. <br><br>

            Seit 2005 bin ich Mitglied des Deutschen Bundestages. Von 2005 bis 2009 war ich ordentliches Mitglied im Rechtsausschuss und im Ausschuss für Ernährung, Landwirtschaft und Verbraucherschutz. Von 2009 bis 2013 war ich Mitglied im Ausschuss für Bildung, Forschung und Technikfolgenabschätzung und im Rechtsausschuss. Seit der Bundestagswahl 2013 gehöre ich wieder dem Ausschuss für Bildung, Forschung und Technikfolgenabschätzung an sowie dem Ausschuss für Wahlprüfung, Immunität und Geschäftsordnung. Stellvertretendes Mitglied bin ich im Ausschuss für Recht und Verbraucherschutz und
        </p>
    </div>

    <div class="col-lg-6 col-s-12 col-right">
        <p>
            in der Kommission des Ältestenrates für die Rechtsstellung der Abgeordneten sowie für innere Angelegenheiten des Bundestages. Seit 2010 bin ich stellvertretende Vorsitzende der SPD-Landesgruppe Bayern, seit 2013 stellvertretende Sprecherin der AG Wahlprüfung, Immunität und Geschäftsordnung. <br> <br>

            Seit 1996 bin ich Markträtin in Wernberg-Köblitz und Kreisrätin im Landkreis Schwandorf. <br><br>

            Zur Politik kam ich über mein langjähriges ehrenamtliches Engagement in der Katholischen Landjugendbewegung und durch meine Erfahrungen in der Bürgerbewegung gegen die geplante WAA in Wackersdorf. 1990 bin ich der SPD beigetreten und bin Vorsitzende meines Heimat-SPD-Ortsvereins Wernberg-Köblitz. Daneben bin ich Vorsitzende des SPD-Unterbezirks Schwandorf-Cham und stellvertretende Bezirkvorsitzende der Oberpfälzer SPD. Von 2009 bis 2017 war ich Mitglied im Landesvorstand der BayernSPD und von 2011 bis 2017 außerdem Mitglied des Präsidiums der BayernSPD.
        </p>
    </div>

</div>


